#![no_std]
#![no_main]
#![feature(asm)]
#![feature(global_asm)]
#![feature(format_args_nl)]
#![feature(const_generics)]

use user::constant::STDOUT;
use user::println;
use user::syscall::{exec, exit, fork, open, read, write};

#[no_mangle]
pub unsafe extern "C" fn _start() -> ! {
    let p = fork();
    if p == 0 {
        println!("forking test2...");
        exec("/test2", &["test1", "test2"]);
    }
    println!("test1 running...");
    let fd = open("/test.txt", 0);
    let mut data = [0; 32];
    read(fd, &mut data);
    write(STDOUT, &data);
    exit(0);
}
